     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'contact_us' => 'required',
            'contact_us_ar' => 'required',
        ]);

        $setting = Setting::find($id);
        $setting->contact_us = $request->contact_us;
        $setting->contact_us_ar = $request->contact_us_ar;
        $setting->save();

        return redirect()->route('settings.contact-us.index')->with(['message' => __('lang.successfully_updated')]);
    }

