     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'ar' => 'required',

        ]);
        $category = Category::find($id);

        $category->ar = $request->ar;

        if (!empty($request->image)) {
            $filename = md5(uniqid()) . date('dmY') . '.' . $request->image->getClientOriginalExtension();
            $category->image = $request->image->storeAs('', $filename, 'public');
        }
        $category->save();
        return redirect()->route('settings.categories.index')->with(['message' => __('lang.successfully_updated')]);
    }

